namespace dropdowns {
	inline std::unordered_map<std::string, std::unordered_map<std::string, bool>> dropdown_states = {
		{"Turret Options", {{"_open", false}}},
		{"Radar Options", {{"_open", false}}},
		{"Alert Options", {{"_open", false}}},
		{"Pickup Options", {{"_open", false}}},
		{"Flags", {{"_open", false}}},
	};
	inline bool get_isSelected(std::string name, std::string option) {
		// Find the state map for a specific dropdown, e.g., "MyDropdown1"
		if (dropdowns::dropdown_states.find(name) != dropdowns::dropdown_states.end()) {
			std::unordered_map<std::string, bool>& dropdownState = dropdowns::dropdown_states[name];

			if (dropdownState.find(option) != dropdownState.end()) {
				bool isSelected = dropdownState[option];
				if (isSelected) {
					return true;
				}
				else {
					return false;
				}
			}
			else {
				return false;
			}
		}
		else {
			return false;
		}
	}
}

